package com.tx.component.plugin.starter;

import org.springframework.boot.context.properties.ConfigurationProperties;

import com.tx.component.plugin.PluginContextConstants;

/**
 * 基础数据容器默认配置<br/>
 * <功能详细描述>
 * 
 * @author  Administrator
 * @version  [版本号, 2018年4月27日]
 * @see  [相关类/方法]0
 * @since  [产品/模块版本]
 */
@ConfigurationProperties(prefix = PluginContextConstants.PROPERTIES_PREFIX)
public class PluginContextProperties {
    
    /** 命令容器是否启动 */
    private boolean enable;
    
    /** 所属模块 */
    private String module;
    
    /**
     * @return 返回 enable
     */
    public boolean isEnable() {
        return enable;
    }
    
    /**
     * @param 对enable进行赋值
     */
    public void setEnable(boolean enable) {
        this.enable = enable;
    }
    
    /**
     * @return 返回 module
     */
    public String getModule() {
        return module;
    }
    
    /**
     * @param 对module进行赋值
     */
    public void setModule(String module) {
        this.module = module;
    }
}
