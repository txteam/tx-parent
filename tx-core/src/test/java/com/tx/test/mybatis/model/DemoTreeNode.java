public class DemoTreeNode extends Demo implements TreeAble<List<Demo>,Demo>{
    
    private String parentId;
    
    private List<Demo> childs;
    
    private Demo subDemo;

    /**
     * @return 返回 parentId
     */
    public String getParentId() {
        return parentId;
    }

    /**
     * @param 对parentId进行赋值
     */
    public void setParentId(String parentId) {
        this.parentId = parentId;
    }

    /**
     * @return 返回 childs
     */
    public List<Demo> getChildren() {
        return childs;
    }

    /**
     * @param 对childs进行赋值
     */
    public void setChildren(List<Demo> childs) {
        this.childs = childs;
    }

    /**
     * @return 返回 subDemo
     */
    public Demo getSubDemo() {
        return subDemo;
    }

    /**
     * @param 对subDemo进行赋值
     */
    public void setSubDemo(Demo subDemo) {
        this.subDemo = subDemo;
    }
}
